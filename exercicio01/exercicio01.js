document.getElementById("um").onclick = () => {
    let displayAppears = document.getElementById("div1").style.display;
    if(displayAppears === "none"){
        document.getElementById("div1").style.display = 'block';
    }else{
        document.getElementById("div1").style.display = 'none';
    }
}
